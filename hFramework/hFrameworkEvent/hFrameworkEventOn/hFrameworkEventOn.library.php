class hFrameworkEventOnLibrary extends hPlugin {

    private $hFrameworkEvent;

    public function hConstructor()
    {
        $this->hFrameworkEvent = $this->library('hFramework/hFrameworkEvent');
    }

    public function __call($method, $arguments)
    {
        return $this->hFrameworkEvent->on(
            $method,
            $arguments
        );
    }
}

